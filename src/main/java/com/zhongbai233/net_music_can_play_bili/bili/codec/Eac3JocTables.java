    public static final int[][] SUBBAND_TO_BAND = buildSubbandToBand();

    private static int[][] buildSubbandToBand() {
        int[][] result = new int[8][64];
        for (int bi = 0; bi < 8; bi++) {
            int[] starts = BAND_STARTS[bi];
            int nb = NUM_BANDS[bi];
            for (int pb = 0; pb < nb; pb++) {
                int start = starts[pb];
                int end = (pb + 1 < starts.length) ? starts[pb + 1] : 64;
                for (int sb = start; sb < end && sb < 64; sb++) {
                    result[bi][sb] = pb;
                }
            }
        }
        return result;
    }

